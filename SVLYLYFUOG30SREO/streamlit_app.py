import datetime as dt
import streamlit as st
import pandas as pd

# Set page config
st.set_page_config(page_title="flight_operations", layout="wide")

# Title
st.title("flight_operations")

# Initialize Snowflake session
session = st.connection("snowflake").session()

@st.cache_data(ttl="23h50m")
def execute_query(query: str) -> str:
  return session.sql(query).collect_nowait().query_id

def query_1_1() -> str:
  sql_query = r"""
SELECT SUM(ON_GROUND) FROM FLIGHT_KPIS ;  """

  return sql_query

execute_query(query_1_1())

@st.fragment
def cell_1_1():
  with st.container(border=True):
    with st.container(horizontal=True, horizontal_alignment="distribute", vertical_alignment="center"):
      st.markdown("### Total On-Ground")
      if st.button(
        ":material/refresh:", type="tertiary", key=f"refresh_button_cell_1_1", help="Refresh total_on-ground data"
      ):
        execute_query.clear(query_1_1())

    try:
      with st.spinner("Executing query", show_time=True):
        df = session.create_async_job(
          execute_query(query_1_1())
        ).result("pandas")

      if any(df.columns.duplicated()):
        new_names = []
        name_indexes = {}
        for name in df.columns:
          name_index = name_indexes.get(name, 0) + 1
          name_indexes[name] = name_index
          new_names.append(f"{name}_{name_index}" if name_index > 1 else name)
        df.columns = new_names

      # Calculate metric for scorecard
      if len(df) > 0:
        value = df["SUM(ON_GROUND)"].iloc[0]
        st.metric(
          label="SUM(ON_GROUND)",
          value=f"{value:,.0f}"
        )
      else:
        st.warning("No data available")
    except Exception as e:
      st.error(f"Error: {str(e)}")

